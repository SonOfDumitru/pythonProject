'''
Design patterns (sabloane de proiectare) sunt niste solutii tipice la probleme comune in proiectarea software

Fiecare sablon este ca un model care poate fi personalizat pentru a rezolva o anumita problema de design din cod.
Ele definesc un limbaj comun care ajuta echipe de dezvoltare sa comunice mai eficient.
Sabloanele difera prin complexitatea lor, nivelul de detalii si scara de aplicabilitate asupra intregului sistem proiectat.

'''


'''
Tipuri de design patterns:
-creationale -> ofera mecanisme de creare a obiectelor care maresc flexibilitatea si reutulizarea codului existent.
-structurale -> definesc cum sa fie asamblate obiectele si clasele in structuri mai mari pastrand in acelasi timp flexibilitatea si eficenta lor
-comportamentale -> se ocupa de comunicarea eficienta intre obiecte si de repartizarea responsabilitatilor intre acestia
'''
