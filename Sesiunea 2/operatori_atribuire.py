x = 15
x += 5  # -> x =x + 5
print(x)
x -= 5  # -> x =x - 5
print(x)
x *= 5  # -> x =x * 5
print(x)
x /= 5  # -> x =x / 5 -> impartire reala
print(x)
x %= 5  # -> x =x % 5 -> restul impartirii sau "modulo"
print(x)
x //= 5  # x =x // 5 -> impartire intreaga, ne afiseaza doar intregul
print(x)
x **= 5  # x =x ** -> 5 la puterea a 5 a
print(x)


